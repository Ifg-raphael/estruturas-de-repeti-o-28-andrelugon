#Programa recebe cinco valores e calcula a média entre esse valores:

#Entrada de dados: O usuário informa os valores n1, n2, n3, n4 e n5
n1 = int(input())
n2 = int(input())
n3 = int(input())
n4 = int(input())
n5 = int(input())

#Processamento: Cálculo da média dos valores informados
media = (n1 + n2 + n3 + n4 + n5) / 5

#Saída de dados: Imprime o valor da média
print(f"{media}")

